package com.backmantenedor.Util;

import java.util.List;

public class EntryAppPerfil {

    private Boolean flagCreation;

    private List<Long> perfilApp;

    private Long idApp;

    public Boolean getFlagCreation() {
        return flagCreation;
    }

    public void setFlagCreation(Boolean flagCreation) {
        this.flagCreation = flagCreation;
    }

    public List<Long> getPerfilApp() {
        return perfilApp;
    }

    public void setPerfilApp(List<Long> perfilApp) {
        this.perfilApp = perfilApp;
    }

    public Long getIdApp() {
        return idApp;
    }

    public void setIdApp(Long idApp) {
        this.idApp = idApp;
    }


}
